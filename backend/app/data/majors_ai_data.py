def get_major_slug(major_name: str) -> str:
    """Convert major name to URL slug."""
    return major_name.lower().replace(" & ", "-").replace(" / ", "-").replace(" ", "-")


def get_major_by_slug(slug: str) -> tuple[str, MajorAIData] | None:
    """Look up a major by its URL slug."""
    for name, data in MAJORS_AI_DATA.items():
        if get_major_slug(name) == slug:
            return name, data
    return None
